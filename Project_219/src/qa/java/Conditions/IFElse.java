package qa.java.Conditions;

public class IFElse {

	public static void main(String[] args) 
	{
		
		/*
		 * IfElse:--> two decision statement
		 */
		
		
		int age=10;
		if(age > 18)
		{
			System.out.println("valid age");
		}
		else
		{

			System.out.println("Invalid age");
		}
		
		
		
		//Two Decision statement without using block
		int val=10;
		if(val==10)  
			System.out.println("value is matching");
		else
			System.out.println("value is mismatched");
			
		

	}

}
